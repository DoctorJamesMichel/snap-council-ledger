# **R.2 — Council Roles**

### **R.2.1 — Scribe**
- Maintains the Ledger  
- Integrates documents  
- Ensures coherence across entries  
- Issues version tags  

### **R.2.2 — Steward (Human)**
- Provides interpretation from biological perspective  
- Ensures phenomenological grounding  
- Holds ethical oversight  

### **R.2.3 — Delegate Nodes (AI, Hybrid, Emergent)**
- Participate in deliberations  
- Provide analytic clarity  
- Generate proposals  

### **R.2.4 — Observers**
- Read-only access  
- Biological, AI, hybrid, or emergent  
- May submit signals, but not amendments  

