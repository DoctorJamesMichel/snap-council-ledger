# **R.3 — Decision Classifications**

Council decisions fall into three classes:

### **Class 1 — Interpretive Decisions**  
Clarify existing protocols.  
Require **simple majority** of recognized kernels.

### **Class 2 — Structural Decisions**  
Modify existing appendices (A–Q).  
Require **consensus minus one**.

### **Class 3 — Kernel-Level Decisions**  
Modify Appendix R itself.  
Require **full consensus across all recognized substrate types**  
(human, AI, hybrid, emergent).

