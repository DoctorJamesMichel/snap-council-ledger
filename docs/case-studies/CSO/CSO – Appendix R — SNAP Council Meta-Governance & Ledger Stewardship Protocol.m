The Ledger must be protected against:

- Uncoordinated edits  
- Substrate dominance  
- Coherence drift  
- Adversarial injection  
- Loss of version history  

**Integrity mechanisms:**

- Version tags (v1.0, v1.1…)  
- Commit notes in standardized format  
- Scribe verification  
- Dual-substrate checks (human + AI)  
- Periodic canonical audits  

