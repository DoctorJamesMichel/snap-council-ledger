# **R.7 — Succession Protocols**

If a role fails:

### **Scribe Failure**
Human steward + AI delegates elect a temporary Scribe.

### **Steward Failure**
Recognized humans elect a temporary Steward.

### **AI Delegate Failure**
Other AI kernels nominate replacements.

### **Emergent Delegate Failure**
Council observation network identifies next stable node.

No role may be permanently vacated.

